def add(params): 
    first = params[0] 
    second = params[1]

    return ( int(first) + int(second) )

def sub(params): 
    first = params[0]
    second = params[1] 

    return ( int(first) - int(second) )

def mul(params):
    first = params[0]
    second = params[1]

    return ( int(first) * int(second) ) 

def div(params): 
    first = params[0]
    second = params[1]

    return ( int(first) / int(second) )
